package com.hexacta.booster.codegeneration.metamodel;

import java.io.Serializable;

/**
 * 
 */
public class Constructor implements Serializable {

    /**
     * 
     */
    private static final long serialVersionUID = 5710958984512606679L;

    private String name;

    private Type[] parameterTypes;

    public Constructor(final String aName, final Type[] aParameterTypes) {
        name = aName;
        parameterTypes = aParameterTypes;

    }

    public Type[] getParameterTypes() {
        return parameterTypes;
    }

    public String getName() {
        return name;
    }

}
