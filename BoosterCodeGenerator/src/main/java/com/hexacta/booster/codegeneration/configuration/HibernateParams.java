package com.hexacta.booster.codegeneration.configuration;

/**
 * This class keeps some basic parameters for hibernate.
 */

public class HibernateParams {

    private String dataBase;

    private String driver;

    private String dialect;

    private String username;

    private String password;

    private int maxConnections;

    public HibernateParams(final String database, final String driver, final String dialect, final String user,
            final String password, final int maxconnections) {
        dataBase = database;
        this.driver = driver;
        this.dialect = dialect;
        username = user;
        this.password = password;
        maxConnections = maxconnections;
    }

    public String getDatabase() {
        return dataBase;
    }

    public String getDriver() {
        return driver;
    }

    public String getUsername() {
        return username;
    }

    public String getPassword() {
        return password;
    }

    public int getMaxConnections() {
        return maxConnections;
    }

    public void setDatabase(final String database) {
        dataBase = database;
    }

    public void setDriver(final String driver) {
        this.driver = driver;
    }

    public void setUsername(final String username) {
        this.username = username;
    }

    public void setPassword(final String password) {
        this.password = password;
    }

    public void setMaxConnections(final int maxConnections) {
        this.maxConnections = maxConnections;
    }

    public void setDialect(final String dialect) {
        this.dialect = dialect;
    }

    public String getDialect() {
        return dialect;
    }
}
