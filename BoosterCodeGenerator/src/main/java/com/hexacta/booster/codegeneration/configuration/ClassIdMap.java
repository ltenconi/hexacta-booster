package com.hexacta.booster.codegeneration.configuration;

import java.util.HashMap;
import java.util.Map;

/**
 * 
 */
public class ClassIdMap {

    private Map<String, String> ids;

    public ClassIdMap() {
        ids = new HashMap<String, String>();
    }

    public void putId(final String className, final String idName) {
        ids.put(className, idName);
    }

    public String getId(final String className) {
        return ids.get(className);
    }
}
